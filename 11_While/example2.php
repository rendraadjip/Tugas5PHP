<?php // Awalan dari sebuah script PHP 
$x = 1; //deklarasi variabel x

do {
    echo "The number is: $x <br>"; //output dari echo yaitu "The number is" lalu memanggil nilai variabel x yaitu 1
    $x++;//kenaikan variabel $x dengan 1. Kemudian kondisi diperiksa (apakah $x kurang dari atau sama dengan 5?)
} while ($x <= 5); //loop akan terus berjalan selama $x kurang dari atau sama dengan 5
?> <!--Akhiran dari sebuah script PHP-->